errors = []

def print_errors_music():
    for error in errors:
        print()
        print(error[1])

def print_errors_step():
    for error in errors:
        print()
        print(error[0])

def print_errors_all():
    for error in errors:
        print(error)
    print(f"""
-----------------------------------------------------------------------------------------------------\n
                            Errors number: {len(errors)}\n
-----------------------------------------------------------------------------------------------------\n""")

def append_error(text, name):
    errors.append(f"""
-----------------------------------------------------------------------------------------------------\n
                            Error in music: {name}\n
                            STEP:           {text}\n
-----------------------------------------------------------------------------------------------------\n""")
    print(f"""
-----------------------------------------------------------------------------------------------------\n
-----------------------------------------------------------------------------------------------------\n
                            Error in music: {name}\n
                            STEP:           {text}\n
-----------------------------------------------------------------------------------------------------\n
-----------------------------------------------------------------------------------------------------\n""")